                        <p>The majority of Francophone students coming to Canada end up studying in Quebec, Ontario and New
                            Brunswick. With two publicly funded French-language colleges, Ontario offers Francophone
                            students the opportunity to choose from these or 10 French-language and bilingual institutions
                            with university programs. It’s also worth noting that New Brunswick is the only official
                            bilingual province in Canada.</p>
                        <p>Quebec hosted nearly four in five Francophone students in 2021. In comparison, 13% of Francophone
                            students studied in Ontario.</p>
                        <p>Nearly 19 out of 20 of Francophone students approved to study in Canada were enrolled in
                            post-secondary studies in 2021. In comparison, less than 1% of Francophone international
                            students were enrolled in K-12 programs, and under 1% pursued ESL/FSL programs in 2021.</p>
                        <h3>Looking Forward</h3>
                        <p>2021 was a strong year for international education in Canada. The success of Canada’s generous
                            Post-Graduation Work Permit Program (PGWPP) has played a key role in Canada’s growth, with over
                            97% of eligible PGWP applicants approved between January of 2016 and July of 2022.</p>
                        <p>With strong Francophone communities outside of Quebec, Canada is a popular destination country
                            for Francophone students to start their study abroad journey. Increased efforts from the
                            Canadian government to attract more students shows a strong government commitment to support
                            Francophone students in Canada.</p>
                        <p>We expect to see more Francophone students headed to Canada, as a part of the continued growth we
                            are seeing across the country. Schools looking to attract more Francophone students to study in
                            Canada can:</p>
                        <ul>
                            <li>Highlight existing Francophone communities in Canada, within and outside of Quebec.</li>
                            <li>Promote the broad variety of French-language education programs offered in Canada.</li>
                            <li>Encourage students to work with informed representatives to support their recruitment goals.
                            </li>
                            <li>Ensure that students are up to date about Canada’s successful programs to support
                                international students, including the PGWP.</li>
                        </ul>
