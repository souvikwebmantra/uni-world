                        <p>The number of Francophone international students approved to study in Canada has increased
                            year-over-year since 2016 with the exception of 2020. From 2016 to 2021, the number of
                            Francophone international students approved to study in Canada increased by 15,000. This
                            represented a growth of 117% in five years.</p>
                        <p>Following the pandemic, over 21,000 Francophone international students were approved to study in
                            Canada in the first eight months of 2022. This puts Canada back on track to keep growing
                            year-over-year from last year’s high.</p>
                        <h3>Where Are Francophone International Students Travelling From?</h3>
                        <p>Francophone international students are coming to Canada from France, Belgium and French Africa.
                            The chart below breaks down the top 10 countries that Francophone international students are
                            traveling from:</p>
                        <div class="sub-our-story-img-blog">
                            <img src="{{ asset('assets/frontend/assets/images/blogs/02-canada-growing-steadily.png') }}"
                                alt="" />
                        </div>
                        <p>France, Morocco, and Algeria have consistently been the top three source markets for Francophone
                            students in Canada. Since 2016, France has held the number one spot as the top country that
                            Francophone students are coming from.</p>
                        <p>Despite an average drop of one percentage point in approval rates across the top 10 countries of
                            origin, some countries of origin saw an increase in approval rates from 2019. Showing strong
                            growth, Belgium more than doubled in new study permit approvals from 2019 to 2021. Senegal,
                            Haiti, and Lebanon also saw an increase in approval rates from 2021 into early 2022.</p>
                        <p>Since August 2022, Francophone students from Congo, Cameroon, Haiti, Guinea, and Mali have
                            surpassed full year study permit approvals from 2021.</p>
                        <h3>What Do Francophone Students Study in Canada?</h3>
                        <p>Some schools in Canada offer programs that combine classroom studies with sightseeing and
                            cultural activities to broaden the student experience and build connections. Schools may also
                            offer work-study programs for students to practice their skills in the workplace. This allows
                            students to explore their school environments and build industry connections to launch into the
                            workforce.</p>
                        <p>The chart below breaks down the percentage of Francophone international students by study level.
                        </p>
                        <div class="sub-our-story-img-blog">
