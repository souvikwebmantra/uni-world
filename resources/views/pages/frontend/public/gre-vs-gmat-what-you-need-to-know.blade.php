                            <ul>
                                <li><strong>GRE:</strong> The GRE is one of the most widely accepted standardized test for
                                    graduate school or business school in English-speaking countries, and is accepted in
                                    over 90 countries. Applicants taking the GRE can choose between the GRE General Test or
                                    the GRE Subject Test. The GRE general test measures an applicant’s general academic
                                    ability to think and communicate, while the GRE subject test looks at an applicant’s
                                    knowledge in a specific domain and is meant for applicants with a strong background or
                                    undergraduate major in a specific STEM discipline.</li>
                                <li><strong>GMAT:</strong> The GMAT is a standardized test for Master’s of Business
                                    Administration (MBA) applicants. According to the Graduate Management Admissions
                                    Council, more than 2,400 schools in 110 countries use the GMAT exam as part of the
                                    selection criteria for their programs.</li>
                            </ul>
