                            <h3>GRE & GMAT Benefits</h3>
                            <p>In addition to providing schools with standardized test scores, taking the GRE or GMAT can
                                have added academic, professional, and financial benefits for students who are planning to
                                study at the graduate level.</p>
                            <ul>
                                <li><strong>GRE:</strong> Admissions and fellowship committees use GRE results to complement
                                    undergraduate records, recommendation letters, and other applicant qualifications.
                                    Taking a GRE Subject Test can help your students stand out from other postgraduate
                                    candidates when applying to a competitive program. In addition, getting a good score on
                                    the GRE can bring further benefits, such as helping students receive scholarships or
                                    teaching assistant positions.</li>
                                <li><strong>GMAT:</strong> Business schools see GMAT scores as a measure of future MBA
                                    student success because the GMAT looks at how students can apply their knowledge in
                                    real-world business situations. The GMAT evaluates problem-solving, logic, critical
                                    reasoning, and writing skills which contribute to an applicant’s success in business
                                    programs.</li>
                            </ul>
                            <h3>Test Score System</h3>
                            <p>There are two parts to the GRE and GMAT results. The first part is the score, which varies by
                                test type, and the second part is a percentile ranking for each of the scores.</p>
                            <p>The percentile ranking tells applicants and their prospective schools how well they scored
                                compared to other test takers. So a score with a percentile ranking of 80, means the
                                applicant scored higher than 80% of applicants and equally or lower than 20% of applicants.
                                Note, that because rankings are recalculated every year and GRE/GMAT test scores are valid
                                for multiple years, your student’s percentile may change over time.</p>
                            <ul>
                                <li><strong>GRE:</strong> The raw GRE test score (number of questions answered correctly) is
                                    converted to a scaled score. Scaled scores are generally between 130 and 170 for the GRE
                                    General. For GRE Subject tests, the usual score range is between 320 and 990.</li>
                                <li><strong>GMAT:</strong> Applicants that have taken the GMAT test will receive scores for
                                    each of the four test sections, plus a total score. The total score is based on
                                    performance on the verbal and quantitative sections. Total scores for GMAT are reported
                                    in 10-point intervals from 200–800 (e.g. 500, 510, 520). According to the Graduate
                                    Management Admission Council (GMAC), two thirds of GMAT test-takers score between 400
                                    and 800.</li>
                            </ul>
                            <h3>Test Content</h3>
                            <p>The GRE General and the GMAT are both adaptive, which means that the difficulty of future
                                questions depends on how applicants do early in the test. </p>
                            <ul>
                                <li><strong>GRE:</strong> The contents of GRE Subject tests vary by area of study
                                    (Chemistry, Physics, Math, or Psychology). The GRE General test contains three sections,
                                    including verbal reasoning, quantitative reasoning, and analytical writing.</li>
                                <li><strong>GMAT:</strong> The GMAT contains four sections including analytical writing,
                                    integrated reasoning, quantitative reasoning, and verbal reasoning. These sections
                                    evaluate the applicant’s skills in critical thinking and communication, data analysis
                                    and information interpretation, numerical literacy and math as well as editing and
                                    reading comprehension.</li>
                            </ul>
                            <h4>Wondering how the tests are structured, how to help your students prepare, and where to get
