                            <h3>Length & Format of Test</h3>
                            <ul>
                                <li><strong>GRE:</strong> Applicants can complete the GRE General Test at a testing center
                                    as a paper-based test or as an at-home, computer-based test. The GRE takes about 3 hours
                                    and 45 minutes to complete. The GRE subject test is a paper based and is offered up to
                                    three times every year in September, October, and/or April. Testing times vary, so it is
                                    important to check with nearby test centers early to reserve spots.</li>
                                <li><strong>GMAT:</strong> Applicants can complete the GMAT online or on computers at GMAT
                                    test centers. Students have up to 3 hours and 7 minutes to complete the test.</li>
                            </ul>
