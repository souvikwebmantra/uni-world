                            <h3>Three Sections of the PTE Academic Exam</h3>
                            <p class="pb-0"><strong>Section 1: Speaking & Writing</strong></p>
                            <p class="pb-0"><strong>Time:</strong> 54–67 minutes</p>
                            <p><strong>Questions:</strong> 7 different types of questions</p>
                            <ul class="sub-unver-content-list">
                                <li><strong>Intro (not scored):</strong> The intro section is a single question that is not
                                    scored. This helps students get ready to complete the rest of the PTE exam. There will
                                    be a prompt asking the student about themselves. Your student will have 25 seconds to
                                    read the prompt and prepare their answer, then they’ll have 30 seconds to respond.</li>
                                <li><strong>Read a prompt:</strong> A prompt of up to 60 words will appear, followed by a
                                    tone. After the tone, the student has to read the prompt out loud. They’ll be scored on
                                    three things: content, oral fluency, and pronunciation. For content, they’ll be looking
                                    to make sure your student only uses the exact words from the prompt. For oral fluency,
                                    they’ll look at the natural rate at which the student speaks and how smooth and
                                    effortless their reading sounds. Finally, for pronunciation, they’ll evaluate if the
                                    student pronounced words correctly. The PTE exam will not penalize a student’s accent,
                                    but they will check if a student stresses the right vowels and consonants.</li>
                                <li><strong>Repeat the sentence:</strong> The student will be asked to listen to a sentence
                                    and then repeat the sentence. Each ‘repeat the sentence’ question should only take 3–9
                                    seconds.</li>
                                <li><strong>Describe an image:</strong> An image will appear on your student’s screen for 25
                                    seconds. During this time, your student can prepare their answer with point-form notes.
                                    They’ll then have 15 seconds to describe the image in as much detail as they can.</li>
                                <li><strong>Re-give a lecture:</strong> Your student will be asked to listen to a recording
                                    of a real lecture lasting up to 90 seconds. They’ll then have 10 seconds to prepare
                                    before re-giving the lecture in their own words. Their answer can be up to 40 seconds
                                    long.</li>
                                <li><strong>Short Answer:</strong> Your student will listen to a short question (3–9 seconds
                                    long). Then, they will need to give a brief answer lasting no more than 10 seconds. When
                                    appropriate, your student’s answer can be as short as a single word for this section.
                                </li>
                                <li><strong>Text summary:</strong> Your student will read a text that is no more than 300
                                    words. Then they’ll have 10 minutes to create a single sentence summary of the text.
                                </li>
                                <li><strong>Essay:</strong> Your student will have 20 minutes to write a 200–300 word essay
                                    on a prompted topic. They can use the cut, copy, and paste features to move text around
                                    to edit their response.</li>
                            </ul>
                            <p class="pb-0"><strong>Section 2: Reading</strong></p>
                            <p class="pb-0"><strong>Time:</strong> 30 minutes</p>
                            <p><strong>Questions:</strong> 5 questions</p>
                            <ul class="sub-unver-content-list">
                                <li><strong>Fill in the blank:</strong> Your student will see a sentence with blank lines
                                    where some words should be. They’ll also see a list of words on the screen. The student
                                    must drag and drop words from the list to fill the blanks in the sentence.</li>
                                <li><strong>Multiple choice (multiple answer):</strong> This question begins with a text up
                                    to 300 words long. Your student will be given a question about the text. They must then
                                    select all the correct answers to the question, not just the most right answer.</li>
                                <li><strong>Re-order paragraphs:</strong> A few text boxes with paragraphs in them will
                                    appear on the screen. These boxes will be in a random order. Your student will need to
                                    move the paragraphs around with their mouse so they are in the correct order.</li>
                                <li><strong>Fill in the gap:</strong> A text of up to 80 words will appear on the screen.
                                    This text will have gaps in a few places. Your student must drag words from the box
                                    below the prompt to the right gaps.</li>
                                <li><strong>Multiple choice (single answer):</strong> Your student will see a prompt up to
                                    300 words long. They’ll then be asked a multiple choice question which has only one
                                    fully correct answer. This means several answers may seem correct, but one will be more
                                    correct than the others. This question could be about specific content in the prompt or
                                    it could be about the tone of the prompt.</li>
                            </ul>
                            <p class="pb-0"><strong>Section 3: Listening</strong></p>
                            <p class="pb-0"><strong>Time:</strong> 30–43 minutes</p>
                            <p><strong>Questions:</strong> 8 questions consisting mostly of audio and video clips</p>
                            <ul class="sub-unver-content-list">
                                <li><strong>Summarize spoken text:</strong> Your student will listen to a 60–90 second
                                    prompt and then be expected to write a 50–70 word summary. They’ll have 10 minutes to
                                    write the summary. The key for this question is to take quick notes as the prompt plays
                                    to help remember all the points relevant to the summary.</li>
                                <li><strong>Multiple choice (multiple answer):</strong> A 40–90 second prompt will play,
                                    then the student will need to answer a multiple choice question which may have multiple
                                    answers. For this type of question, the student should select every answer which is
                                    correct, not just the one answer that seems the most correct.</li>
                                <li><strong>Fill in the blanks:</strong> Your student will see a transcript appear on the
                                    screen with words missing. They’ll also hear a 30–60 second audio prompt of the same
                                    text without words missing. The student will need to write the missing words in the
                                    correct blank.</li>
                                <li><strong>Highlight correct summary:</strong> After listening to a 30–60 prompt, the
                                    student must select the paragraph which best summarizes the prompt they just heard.</li>
                                <li><strong>Multiple choice (single answer):</strong> After listening to a 30–90 second long
                                    audio prompt, the student must choose the paragraph which best summarizes the prompt.
                                </li>
                                <li><strong>Select the missing word:</strong> Your student must select the missing word
                                    after listening to a 20–70 second prompt. The word they select will come from a list of
                                    words available to them. Only one word is the correct choice.</li>
                                <li><strong>List incorrect words:</strong> In this question, your student will listen to a
                                    15–20 second prompt. Then, they’ll need to identify words in the transcript which are
                                    different from what was actually said in the prompt.</li>
                                <li><strong>Write the sentence:</strong> Your student will listen to a one-sentence audio
                                    prompt and then be expected to write the sentence down exactly as they heard it.
                                    Spelling counts in this response.</li>
                            </ul>
