                            <h3>Additional Tips</h3>
                            <p>During the PTE exam, audio and videos will only play once. They will not replay, so ensure
                                your student knows to listen carefully and be prepared! You can suggest they take point-form
                                notes as they listen or watch the prompt.</p>
                            <p>You should also advise your students to take any English language test a few months before
                                the intake date of their chosen program. This allows them time to prepare and retake the
                                test if needed. Be sure to provide your students with free online PTE practice tests and
                                resources to help them prepare. This helps ensure your students can present their best score
                                to prospective schools and maximize their success.</p>
