                            <h3>What is the Pearson PTE Academic Exam?</h3>
                            <p>The Pearson Academic and Pearson Academic UKVI are English language exams. These exams are
                                required by schools and governments to show that your student will be able to study in
                                English. English language tests are required by most higher education institutions (HEIs)
                                and governments for international students from non-English speaking countries. Many
                                students looking to study abroad in the UK, US, Australia, New Zealand, and Canada will need
                                to complete such an English test.</p>
                            <p>The Pearson Academic and Pearson Academic UKVI use the same questions and the tests are
                                scored the same. However, the PTE UKVI is only for study abroad applications to the UK. The
                                UKVI should be used when your student requires a SELT Unique Reference Number (URN) for
                                their visa application.</p>
                            <p>While PTE now offer a PTE Academic Online exam, this article details in-person PTE exams.</p>
                            <h4>Curious about the different types of PTE Tests & Vouchers? Click here to learn more about
                                the similarities and differences between PTE Academic, PTE UKVI, PTE A1, PTE A2 and PTE B1.
                            </h4>
                            <h3>Test Format</h3>
                            <p>The PTE Academic exam measures your student’s English speaking, listening, reading, and
                                writing skills. Testing of all these skills takes place in a single 2 hour session. The PTE
                                Academic exam requires some security measures, so the test is administered at test centers
                                run by Pearson worldwide. PTE test centers have no more than 15 seats per room, so your
                                student will be able to take their test comfortably.</p>
                            <p>The test is taken on a computer, meaning your student will have to read and listen to prompts
                                and enter their answers into a computer. The test integrates the different skills together
                                (for example, speaking and writing) as much as possible.</p>
                            <p>The PTE Academic test measures what Pearson calls “real life English”. In other words, the
                                test is designed to mimic what your student will encounter in the real world as closely as
                                possible. To do this, the test includes speakers with accents and content from real lectures
                                (instead of a fake lecture script). Students will have limited time for each question.
                                However, always encourage your students to review their answers before they submit.</p>
