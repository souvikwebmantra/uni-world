            <h3>About the Deloitte Technology Fast 50™ Program</h3>
            <p>The Deloitte Technology Fast 50 program is Canada’s pre-eminent technology awards program. Celebrating its 25th anniversary, the program recognizes business growth, innovation, and entrepreneurship in four distinct categories: Technology Fast 50 ranking, Enterprise Fast 15, Clean Technology, and Companies-to-Watch.</p>
            <p>The program also recognizes companies within the North American Technology Fast 500 ranking, identifying thriving technology companies in the United States and Canada.</p>
            <p>The 2022 program sponsors include Deloitte, RBCx, Osler, EDC, CBRE, Vector Institute, Council of Canadian Innovators (CCI), Clarity Recruitment, Lafond, and TMX. For further information, visit www.fast50.ca.</p>
            <h3>About Uniwolc</h3>
            <p>Uniwolc empowers students around the world to access the best education. To date, Uniwolc has become the world’s largest online platform for international student recruitment, assisting over 400,000 students with their educational journeys. Founded in 2015, the company has built partnerships with over 1,600 primary, secondary, and post-secondary schools, and works with over 10,000 recruitment partners to drive diversity on campuses across Canada, the United States, the United Kingdom, Australia, and Ireland.</p>
