                        <h3>What is Working in Design and Illustration Like? </h3>
                        <p>Studying illustration and design sets grads up for so many different careers. You could create
                            company branding, illustrate a children’s book, master web design, work in fashion, or produce
                            theatre, TV, or film. </p>
                        <p>Other design grads work with multidisciplinary teams:</p>
                        <ul>
                            <li><strong>Product designers</strong> work with other creatives and manufacturing teams to
                                decide what physical objects (from bookshelves to blenders) will look like. </li>
                            <li><strong>Architectural designers</strong> use computer-aided drawing (CAD) software to create
                                building plans based on architectural sketches, working closely with architects and
                                engineers.</li>
                            <li><strong>Video game designers</strong> use software and illustration skills to create game
                                elements like visual effects and character design.</li>
                        </ul>
                        <p>If you can adapt quickly and enjoy working with lots of different kinds of people, you can excel
                            in these roles.</p>
                        <p>Some creative grads work on a freelance basis. This can be appealing because you pick which work
                            you want to do and when you want to do it. As a freelancer, you’re your own boss. When you
                            freelance full-time, you choose where you want to work (at home? a cafe? a coworking space?),
                            can contract with clients worldwide, and you keep all the profits! However, it also means you
                            have to find the work, manage estimates and billing, and promote your skills—in addition to the
                            creative tasks you’re hired for.</p>
                        <p>Working freelance full-time works well if you want to build your business and communication
                            skills, prefer working on your own schedule, or if you aren’t a primary income earner for your
                            household. However, freelancing isn’t the best fit if you like a regular schedule or want access
                            to work-supplied benefits.</p>
