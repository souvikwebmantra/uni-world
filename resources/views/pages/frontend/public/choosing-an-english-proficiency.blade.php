                            <h3>Length & Format of Test</h3>
                            <p>Each test takes a different length of time, based on the test format.</p>
                            <ul>
                                <li><strong>TOEFL:</strong> TOEFL iBT tests take around 3 hours to complete. The test is
                                    divided into reading (54-72 minutes), listening (41-57 minutes), speaking (17 minutes),
                                    and writing (50 minutes). TOEFL tests can be completed on paper or on a computer.</li>
                                <li><strong>IELTS:</strong> IELTS tests have a total test time of 2 hours and 45 minutes.
                                    The test is divided into listening (30 minutes), reading (60 minutes), writing (60
                                    minutes), and speaking (11-14 minutes). The first three sections are completed together
                                    on paper while the speaking test is done separately.</li>
                                <li><strong>PTE Academic:</strong> PTE Academic tests are completed in one 2 hour session.
                                    The test is divided into speaking & writing (54-67 minutes), reading (29-30 minutes) and
                                    listening (30-43 minutes). PTE Academic tests are completed on a computer.</li>
                            </ul>
