                            <ul>
                                <li><strong>TOEFL:</strong> The written part of a TOEFL test includes multiple choice,
                                    completion, categorization, short answer, and essay questions. The spoken part of the
                                    test includes a presentation, a short reading, and two short summaries.</li>
                                <li><strong>IELTS:</strong> The written portion of the IELTS test features a variety of
                                    multiple choice, matching, completion, short-answer, and essay questions. The spoken
                                    portion of the test includes an oral interview with questions and a short presentation.
                                </li>
                                <li><strong>PTE:</strong> PTE tests feature multiple choice, short answer, summary, and
