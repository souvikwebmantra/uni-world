                            <h3>Length & Format of Test</h3>
                            <ul class="blog-list-two-mt">
                                <li><strong>TOEFL:</strong> Scored on a total scale from 0 to 120, with each section scored
                                    between 0 and 30. TOEFL tests are marked by both humans and computers. Most schools
                                    require a minimum overall score of 80-90.</li>
                                <li><strong>IELTS:</strong> Scored on a scale from 0 to 9. IELTS tests are exclusively
                                    marked by humans. Most schools require a minimum overall score of at least 6.</li>
                                <li><strong>PTE Academic:</strong> Scored on a scale from 10 to 90. PTE tests are graded by
                                    automated systems. Most schools require a minimum overall score of at least 50.</li>
                            </ul>
                            <p>It’s important to note that for competitive programs and schools, higher minimum scores will
                                be needed.</p>
                            <h3>Test Content</h3>
                            <p>While TOEFL and IELTS tests have similar formats, PTE tests have different content. These
