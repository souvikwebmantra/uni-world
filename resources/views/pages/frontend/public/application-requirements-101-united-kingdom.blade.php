                                                        part of the admission process. Standardized English language tests
                                                        evaluate students on their reading, listening, speaking and writing
                                                        skills.</p>
                                                    <p>The following tests are commonly accepted for entry into degree-level
                                                        courses:</p>
                                                    <ul>
                                                        <li>International English Language Testing System (IELTS)</li>
                                                        <li>Test of English as a Foreign Language (TOEFL) - Internet-Based
                                                            Test (IBT)</li>
                                                        <li>Pearson Test of English (PTE)</li>
                                                        <li>Cambridge Advanced Certificate in English (CAE)</li>
                                                    </ul>
                                                    <p>Language test scores required for admission vary by institution and
                                                        by program. Some institutions only look at the overall scores, while
                                                        others have cut-offs for scores in each of the four skills sections.
                                                        Typically, students applying to an undergraduate course need:</p>
                                                    <ul>
                                                        <li>IELTS scores between 5.5 and 6.5 (usually 6.0), with no band
                                                            less than 5.5 or 6.0, or</li>
                                                        <li>TOEFL (internet-based test, iBT) scores between 60.0 and 90.0
                                                            overall, or</li>
                                                        <li>PTE Academic score between 50 to 64 overall, or</li>
                                                        <li>CAE or CPE score between 169 to 176 overall, with no less than
                                                            169 in each category.</li>
                                                    </ul>
